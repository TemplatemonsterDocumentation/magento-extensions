<h2 class="item1">Introduction</h2>
<p>
    <strong>Catalog Image Grid</strong> module serves for displaying a list of categories and subcategories on any page. You can add an image for each category/subcategory and display a listing of subcategories for a category.
</p>
<figure class="img-polaroid"><img src="img/magento/catalog-images-1.jpg" alt="" /></figure>
You can also display a listing with no images:
<figure class="img-polaroid"><img src="img/magento/catalog-images-2.jpg" alt="" /></figure>
Module settings are divided into three main parts:
<ul class="marked-list">
	<li>Catalog page settings</li>
	<li>Widget settings</li>
	<li>Product categories settings</li>
</ul>
