<h2 class="item1">Введение</h2>
<p>
    Модуль <strong>Catalog Image Grid</strong> позволяет Вам отобразить список категорий и подкатегорий на любой странице. К каждой категории/подкатегории у Вас есть возможность прикрепить картинку и отобразить список подкатегорий данной категории.
</p>
<figure class="img-polaroid"><img src="img/magento/catalog-images-1.jpg" alt="" /></figure>
Есть также возможность вывести список подкатегорий без картинок:
<figure class="img-polaroid"><img src="img/magento/catalog-images-2.jpg" alt="" /></figure>
Настройки модуля разбиты на три основных раздела:
<ul class="marked-list">
	<li>Настройки страницы каталога</li>
	<li>Настройки виджета</li>
	<li>Настройки категорий товаров</li>
</ul>
