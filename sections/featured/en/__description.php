<h2 class="item1">Introduction</h2>
The products will refer to:
<ul class="marked-list">
  <li><strong>Bestsellers</strong> - allows displaying products with the highest amount of sales in last 6 month.</li>
  <li><strong>Top rated products</strong> - products that have the highest rating among the customers and site guests.</li>
  <li><strong>Most viewed products</strong> - products that are most frequently viewed by visitors of your website.</li>
  <li><strong>Sale</strong> - products with a discount.</li>
  <li><strong>New products</strong> - allows displaying the latest products added to the store. </li>
  <li><strong>Manual products</strong> - allows displaying any products that you specify in the Choose custom products option.</li>
</ul>
<p>For each of these types, you can manually specify the products that you want to display in this or that ot this block. To do this, you need to select products in <strong>Choose custom products</strong> option.</p>
