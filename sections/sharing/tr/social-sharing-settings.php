<h3>Api Configuration Options</h3>
<p>Bu bölüm Addthis servisinin API ayarlarını içermektedir.</p>
<figure class="img-polaroid"><img src="img/magento/sharing-13.jpg" alt="" /></figure>
<figure class="img-polaroid"><img src="img/magento/sharing-14.jpg" alt="" /></figure>
<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Exclude Services </strong></td>
        <td>-</td>
        <td>menülerden çıkartılacak olacak hizmetler. Örneğin, buraya 'facebook, myspace' yazarsanız Facebook ve MySpace tüm neülerden kaldırılır.</td>
    </tr>     
    <tr>
        <td class="col-1"><strong>Compact Menu Services </strong></td>
        <td>-</td>
        <td>kısa menüdeki hizmetler. Örneğin, buraya 'print, email, favorites' yazarsanız, sadece bu üç hizmet gözükür.</td>
    </tr>  
    <tr>
        <td class="col-1"><strong>Expanded Menu Service </strong></td>
        <td>-</td>
        <td>geniş menüdeki hizmetler. Az seçenek için oldukça kullanışlıdır çünkü services_exclude ile hazırlanacak uzun bir liste yorucu olabilir ve daha sonra yeni bir hizmet eklemekte zorluk yaşanabilir.  Örneğin, buraya 'bebo, misterwong, netvibes' yazarsanız, geniş menüde sadece bu üç hizmet gözükür.</td>
    </tr>   

    <tr>
        <td class="col-1"><strong>Compact Menu Hover </strong></td>
        <td>-</td>
        <td>düğme üzerine fareyle gelindiğinde kısa menünün açılmamasını sağlar. Bunun yerine, düğmeye basıldığında ortaya çıkar.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Delay </strong></td>
        <td>-</td>
        <td>fare düğmenin üzerine geldiğinde kısa menünün açılması için mişisaniye bazındaki bekleme süresi.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Compact Menu Direction </strong></td>
        <td>-</td>
        <td>kısa menüyü normalde, kullanıcının tarayıcısındaki en boş alana doğu gösteririz. Buradan özel bir yön belirleyebilirsiniz.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>New Window </strong></td>
        <td>-</td>
        <td>evet olarak işaretlenirse, AddThis düğmesine basıldığında JavaScript olmaksızın yeni bir pencere açılır.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Menu Language </strong></td>
        <td>-</td>
        <td>menüyü belirlenmiş olan dilde kullanmaya zorla.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Offset Top </strong></td>
        <td>-</td>
        <td>kısa menünün, ilişkilendirilmiş ögelerle arasındaki üst boşluk aralığı. Piksel olarak.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Offset Left </strong></td>
        <td>-</td>
        <td>kısa menünün, ilişkilendirilmiş ögelerle arasındaki sol boşluk aralığı. Piksel olarak.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Load AddThis CSS </strong></td>
        <td>-</td>
        <td>standart CSS dosyasını kapatarak, kendi stil dosyanızı hazırlayabilirsiniz.</td>
    </tr>
