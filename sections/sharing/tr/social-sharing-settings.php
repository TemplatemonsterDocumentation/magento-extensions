        <td class="col-1"><strong>Track Clickbacks </strong></td>
        <td>-</td>
        <td>paylaşım yapılırken, URL adreslerinizin sonuna değer ekleyebiliriz. Bunu, AddThis ile paylaşılan bağlantılar aracılığıyla sitenize gelen ziyaretçilerin sayısını izlemek için kullanırız.</td>
    </tr>
    <tr>
        <td class="col-1"><strong>Google Analytics Id </strong></td>
        <td>-</td>
