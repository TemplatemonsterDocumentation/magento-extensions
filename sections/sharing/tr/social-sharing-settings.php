<h2 class="item1">Extension settings</h2>
<h3>Genel</h3>
<p>Modül ayarlarına başlamak için Mağaza -> Ayarlar ->  Templatemonster-> Social Sharing sekmesine tıklayın. Modülü düzenlemek için ihtiyacınız olan her şeyi burada bulabilirsiniz.<br>Bu bölümde 2 ana seçenek vardır.</p>
<figure class="img-polaroid"><img src="img/magento/sharing-2.jpg" alt="" /></figure>
<table  class="options-table">
    <tr>
        <td class="col-1"><strong>Enabled </strong></td>
        <td>-</td>
        <td>modül etkin/kapalı;</td>
    </tr>     
    <tr>
        <td class="col-1"><strong>Profile id </strong></td>
        <td>-</td>
        <td>Addthis kimliği.</td>
    </tr>    
</table>
<p>
    Kimlik edinmek için, <a target="_blank" href="https://www.addthis.com/">Addthis</a> sitesinden kayıt oluşturun. Sonra, ön panelden yeni proje oluşturun. Bunu yaparken, Genel ayarlar bölümüne yönlendirileceksiniz, kimliğinizi oradaki Genel alanında görebilirsiniz. Bu kimliği kullanmanız gerekiyor.
</p>
<figure class="img-polaroid"><img src="img/magento/sharing-3.jpg" alt="" /></figure>
<h3>Look &amp; Feel</h3>
<p>Bu bölümde, ikonların nasıl gözükeceğini seçebilirsiniz. Hazır veya kendi yükleyeceğiniz ikonları kullanabilirsiniz.</p>
<figure class="img-polaroid"><img src="img/magento/sharing-4.jpg" alt="" /></figure>
<p>
    Kendi ikonlarınızı yükleyin. Bu bölümde 2 seçenek bulunur:
</p>
<figure class="img-polaroid"><img src="img/magento/sharing-5.jpg" alt="" /></figure>
<p><strong>Custom button</strong> - bu alana görselinizin bağlantı adresini yazın. Bağlantıya tıkladığınızda, paylaşma servisini seçebileceğiniz bir pencere açılır.</p>
<figure class="img-polaroid"><img src="img/magento/sharing-7.jpg" alt="" /></figure>
<p>
    <strong>Custom code</strong> - bu seçenekte, tüm ikonları özelleştirebilirsiniz. Alana html ve css kodu girebilirsiniz. Facebook, Twitter veya benzer bir sitenin ikonlarını eklemek isterseniz; bağlantıya uyumlu bir sınıf eklemelisiniz. Facebook için class="addthis_button_facebook", Twitter için class="addthis_button_twitter", sosyal ağ seçenekleri için açılır pencere ise class="addthis_button_more" şeklinde eklenmelidir.
</p>
