<h2 class="item1">Introduction</h2>
<p><strong>Theme Updater</strong> is a Magento 2 extension that allows you to receive updates for installed themes
    purchased at Templatemonster.com. Module periodically searches for updates and notifies you if any
    available. You can also check for updates manually at the module configuration page.</p>

